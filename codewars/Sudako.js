function doneOrNot(board) {
    console.log({board})
  //your code here
  let columns = [[], [], [], [], [], [], [], [], []];
  let box = [[], [], [], [], [], [], [], [], []];
  for (let i = 0; i < board.length; i++) {
    const row = [...new Set(board[i])];
    if (row.length !== 9) return 'Try again!';

    for (let l = 0; l < row.length; l++) {
      columns[l].push(row[l]);

      if (l < 3) {
        // first 3 boxes
        box[l].push(row[l]);
        box[l].push(row[l + 1]);
        box[l].push(row[l + 2]);
      } else if (l < 6) {
        box[l].push(row[l]);
      }
    }
  }
  columns.map((each) => {
    if ([...new Set(each)].length !== 9) return 'Try again!';
  });

  const area = (obj) => {
    let sumNums = 0;
    for (let k = obj.start; k < obj.fin + 1; k++) {
      let colNum = columns[k];
      for (let l = 0; l < 3; l++) {
        sumNums = sumNums + colNum[obj.rows[l]];
      }
    }
    console.log({ sumNums });
    if (sumNums !== 45) return 'Try again!';
  };
  area({ start: 0, fin: 2, rows: [0, 1, 2] });
  area({ start: 0, fin: 2, rows: [3, 4, 5] });
  area({ start: 0, fin: 2, rows: [6, 7, 8] });
  area({ start: 3, fin: 5, rows: [0, 1, 2] });
  area({ start: 3, fin: 5, rows: [3, 4, 5] });
  area({ start: 3, fin: 5, rows: [6, 7, 8] });
  area({ start: 6, fin: 8, rows: [0, 1, 2] });
  area({ start: 6, fin: 8, rows: [3, 4, 5] });
  area({ start: 6, fin: 8, rows: [6, 7, 8] });
  return 'Finished!';
}

